package oop_concept;

public class Data {

	int x = 99;

	public static void main(String[] args) {
		Data obj1 = new Data();
		obj1.x = 199;
		System.out.println(obj1.x);
		
		Data obj2 = new Data();
		System.out.println(obj2.x=55);
		
		Data obj3 = new Data();
		System.out.println(obj3.x=909);
		
		Data obj4 = new Data();
		System.out.println(obj4.x=101);
	}

}
